def sq(x):
    return x * x  # This function calculates the square of x.

def sqr(x):
    return x ** 0.5  # This function calculates the square root of x. Consider renaming to `sqrt` for clarity.

def div(x):
    return x % 2  # This function calculates the modulus of x by 2. The name `div` is misleading and should be updated.
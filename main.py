def sqr(x):  
    # Function to calculate the square of a number  
    return x*x  

def mod(x):  
    # Function to calculate the remainder when a number is divided by 10  
    return x%10  

def floor(x):
    return x//10

a=20
# Assigning the value 10 to variable 'a'  

print(sqr(a))  
# Printing the square of 'a' by calling the sqr function

print(mod(a))  
# Printing the remainder of 'a' divided by 10 by calling the mod function